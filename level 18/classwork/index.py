# For Loop:

for i in range(0, 100):
    print(i)

for i in range(0, 101):
    print(i)

for i in range(10, 20):
    print(i)

for i in range(100, 201, 5):
    print(i)

for i in range(10,  -1, -1):
    print(i)


# While Loop:
    
num = 1
while num <= 10:
    print(num)
    num += 1

num = 0
while num <= 100:
    print(num)
    num += 2


num = 10
while num >= 0:
    print(num)
    num -= 1

    
