# mathematycal operators - მათემატიკური ოპერატორები (+, -, *, /)

# მიმატება (+)

print(5 + 5) # integer --> integer + integer
print(5 + 24) # integer --> integer + integer
print(10.0 + 5) # float --> float + integer


# გამოკლება (-)

print(10 - 5) # integer --> integer - integer
print(0 - 5) # integer --> integer - integer
print(-5.25 - 5) # float --> float - integer
print(10.25 - 5.25) # float --> float - float


# გამრავლება (*)

print(5 * 5) # integer --> integer * integer
print(-5 * 4) # integer --> integer * integer
print(-5.0 * 4) # float --> float * integer


# გაყოფა ( /, // )
print(10 / 2) # float --> integer / integer
print(29 // 5) # integer --> integer // integer


# ნაშთის გამოთვლა (%)

print(29 % 5) # integer % integer
print(24 % 6) # integer % integer
print(11 % 5.5) # integer % float


