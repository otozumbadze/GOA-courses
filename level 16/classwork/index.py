# 1) კომენტარების სახით ახსენით რა არის for loop, როდის ვიყენებთ და რა დანიშნულება გააჩნია. ასევე გააკეთეთ მარტივი მაგალითი.


# for loop არის ციკლი, რომელიც იმეორებს მოქმედებას იმდენჯერ, რამდენჯერაც ჩვენ ვუთითებთ.
# ვიყენებთ მაშინ, როდესაც გვინდა დავამუშავოთ ელემენტები სიიდან,
# ან დავბეჭდოთ რიცხვები გარკვეულ შუალედში, ან რაიმე მოქმედება გავაკეთოთ მრავალჯერ.
# მისი დანიშნულებაა automate — ავტომატურად შეასრულოს განმეორებადი მოქმედებები.

# მარტივი მაგალითი:
for i in range(5):  # range(5) ქმნის რიცხვებს 0-დან 4-მდე
    print("Hello", i)


# 2) კომენტარების სახით ახსენით რა არის indentation და რა დანიშნულება აქვს.

# indentation არის ზოლი (tab ან 4 space), ანუ მარცხნიდან დაშორება.
# Python-ში ის აჩვენებს რომელი კოდი რა ბლოკს ეკუთვნის.
# მაგალითად, for-ის შიგნით მყოფი კოდი აუცილებლად უნდა იყოს indent-ით.


# 3) for ციკლის მეშვეობით გამოიტანეთ რიცხვები 0-დან 67-მდე.
for i in range(0, 68):
    print(i)


# 4) for ციკლის მეშვეობით გამოიტანეთ რიცხვები 12-დან 87-მდე.
for i in range(12, 88):
    print(i)


# 5) for ციკლის მეშვეობით გამოიტანეთ რიცხვები 4-დან 98-მდე, ყოველი 2-ი ნაბიჯის გამოტოვებით.
for i in range(4, 99, 2):  # 2 არის ნაბიჯი
    print(i)


# 6) for ციკლის მეშვეობით გამოიტანეთ თქვენი სახელი 10-ჯერ.
for i in range(10):
    print("Oto")  # რაც გინდა ის დაწერე.


# 7) მომხმარებელს შემოატანინეთ input-ით რაიმე სიტყვა და დაბეჭდეთ ამ სიტყვაში შემავალი ყველა ასო.
word = input("შეიყვანეთ სიტყვა: ")

for letter in word:
    print(letter)


# 8) მომხმარებელმა შემოიტანოს რიცხვი და იგი გამოიყენეთ for-ის ფინალურ მნიშვნელობად.
num = int(input("შეიყვანეთ რიცხვი:"))

for i in range(num):  # num არის ბოლო მნიშვნელობა
    print(i)
